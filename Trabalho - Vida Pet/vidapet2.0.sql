DROP DATABASE IF EXISTS ClinicaVet;

CREATE DATABASE ClinicaVet;

USE ClinicaVet;


CREATE TABLE Clientes (
ID_cliente INT AUTO_INCREMENT,
Nome VARCHAR(255),
CPF VARCHAR(11),
Telefone VARCHAR(15),
Email VARCHAR(255),
Endereco VARCHAR(255),
PRIMARY KEY(ID_cliente)
);


CREATE TABLE Pets (
ID_pet INT AUTO_INCREMENT,
Nome VARCHAR(255),
Especie VARCHAR(100),
Raca VARCHAR(100),
Idade INT,
Peso DECIMAL(5,2),
ID_cliente INT,
PRIMARY KEY(ID_pet),
FOREIGN KEY(ID_cliente) REFERENCES Clientes(ID_cliente)
);


CREATE TABLE Veterinarios (
ID_vet INT AUTO_INCREMENT,
Nome VARCHAR(255),
CRMV VARCHAR(20),
Especialidade VARCHAR(100),
Telefone VARCHAR(15),
PRIMARY KEY(ID_vet)
);


CREATE TABLE Consultas (
ID_consulta INT AUTO_INCREMENT,
Data_consulta DATETIME,
Motivo VARCHAR(255),
Valor DECIMAL(8,2),
ID_pet INT,
ID_vet INT,
PRIMARY KEY(ID_consulta),
FOREIGN KEY(ID_pet) REFERENCES Pets(ID_pet),
FOREIGN KEY(ID_vet) REFERENCES Veterinarios(ID_vet)
);


CREATE TABLE Vacinas (
ID_vacina INT AUTO_INCREMENT,
Nome VARCHAR(255),
Data_aplicacao DATE,
Proxima_dose DATE,
ID_pet INT,
PRIMARY KEY(ID_vacina),
FOREIGN KEY(ID_pet) REFERENCES Pets(ID_pet)
);


CREATE TABLE Exames (
ID_exame INT AUTO_INCREMENT,
Tipo VARCHAR(255),
Resultado VARCHAR(255),
Data_exame DATE,
ID_pet INT,
PRIMARY KEY(ID_exame),
FOREIGN KEY(ID_pet) REFERENCES Pets(ID_pet)
);


CREATE TABLE Pagamentos (
ID_pagamento INT AUTO_INCREMENT,
Valor DECIMAL(8,2),
Forma_pagamento VARCHAR(100),
Data_pagamento DATE,
ID_consulta INT,
PRIMARY KEY(ID_pagamento),
FOREIGN KEY(ID_consulta) REFERENCES Consultas(ID_consulta)
);


